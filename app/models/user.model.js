const { Schema, model } = require('mongoose')

const schema = Schema({
    name: {
        type: String,
        required: true,
        min: 6,
        max: 255
    },
    email: {
        type: String,
        required: true,
        min: 6,
        max: 255
    },
    password: {
        type: String,
        required: true,
        min: 6,
        max: 255
    }
}, { timestamps: true })

module.exports = model('User', schema)